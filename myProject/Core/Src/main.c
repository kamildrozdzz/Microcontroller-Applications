Event ledRedEvent, ledGreenEvent;

void ledRedEventHandler(struct Event* event, uint64_t scheduledTime, void* context) {
    // toggle pin state
    HAL_GPIO_TogglePin(LED_RED_GPIO_Port, LED_RED_Pin);
    // reschedule this event after 500ms
    EVENT_MANAGER_ScheduleEvent(event, scheduledTime + 500);
}

void ledGreenEventHandler(struct Event* event, uint64_t scheduledTime, void* context) {
    // toggle pin state
    HAL_GPIO_TogglePin(LED_GREEN_GPIO_Port, LED_GREEN_Pin);
    // reschedule this event after 700ms
    EVENT_MANAGER_ScheduleEvent(event, scheduledTime + 700);
}

void USART_StressTest(void)
{
    size_t i;
    uint32_t checksum;
    char c;

    // initialize USART
    USART_Init();

    while (1) {
        // reset variables
        i = 0;
        checksum = 0;

        // wait for 10kB of data
        while (i < 10*1024) {
            if (USART_GetChar(&c)) {
                // if character has been received, calculate the checksum
                checksum += c;
                i++;
            } else {
                // delay - this delay introduces the 'tricky' part in the test
                // do not modify it!
                volatile int j;
                for (j=0; j < 10000; j++) {
                    ;
                }
            }
        }
        // after receiving 10kB of data, send out the checksum
        USART_WriteData(&checksum, sizeof(checksum));
    }
}

